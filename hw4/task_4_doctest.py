from typing import List


def fizzbuzz(n: int) -> List[str]:
    """
    >>> fizzbuzz(5)
    ['1', '2', 'fizz', '4', 'buzz']

    >>> fizzbuzz(7)
    ['1', '2', 'fizz', '4', 'buzz', 'fizz', '7']

    >>> fizzbuzz(15)
    ['1', '2', 'fizz', '4', 'buzz', 'fizz', '7', '8', 'fizz', 'buzz', '11', 'fizz', '13', '14', 'fizzbuzz']
    """
    res = []
    for i in range(1, n+1):
        boolean_dict = {
            str(i) : i%3 != 0 and  i%5 != 0,
            'fizz' : i%3 == 0,
            'buzz' : i%5 == 0
        }
        current_number = ''.join( [key for key, value in boolean_dict.items() if value == True])
        res.append(current_number)

    return res

